            <br/>
            <fieldset class="fieldset1">
                <legend>국세청 즉시전송</legend>
                <ul>
                    <li>Response.code : <?php echo $code ?></li>
                    <li>Response.message : <?php echo $message ?></li>
                </ul>
            </fieldset>
         </div>
